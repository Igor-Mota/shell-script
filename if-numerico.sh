# !/bin/bash

# para usar comparaçoes numericas deve se usar o 
# comando test ele atribui o valor do ultimo status 
# de comando para 0 no caso de true e para 1 no caso de false 

if test 1 -eq 1
  then
    echo "1 e igual a 1"
fi

# A tabela de copara tivos e diferente das linguagens de programação tradicionais

# -eq | == | igual
# -ge | >= | maior ou igual
# -gt | >  | maior
# -le | <= | menor ou igual
# -lt | <  | menor
# -ne | != | diferente



